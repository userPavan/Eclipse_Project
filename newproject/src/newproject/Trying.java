package newproject;

public class Trying 
	{
		public Trying(int num, String name, int rollnum) {
		super();
		this.num = num;
		this.name = name;
		this.rollnum = rollnum;
	}
		public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRollnum() {
		return rollnum;
	}
	public void setRollnum(int rollnum) {
		this.rollnum = rollnum;
	}
		private int num;
		private String name;
		private int rollnum;
		
		

	}


